# 10'luk tabandaki sayıyı 2'lik tabana çevirmek
print(bin(4))

# 10'luk tabandaki sayıyı 16'lık tabana çevirmek
print(hex(4))

# Mutlak değer almak
print(abs(-4))
print(abs(5))

# Sayıları yuvrlamak
print(round(4.8))
print(round(4.5))
print(round(3.2224, 3))

# Maximum bulmak
print(max(3, 5, 9, -10, 11, -26.5))
print(min([3, 5, 9, -10, 11, -26]))
print(max((3, 5, 9, -10, 11, -26)))

# Bir diziyi toplamak
print(sum([1, 2, 3.4, 4, 5, 6]))

# Üslü sayı işlemleri yaptırmak
print(pow(2,4))
print(2 ** 4)
